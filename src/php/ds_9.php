        <p>Сигналы индикатора:</p>
        <p>1. Возрастание АДХ всегда указывает на усиление тенденции.
          <br/> 2. Понижение АДХ всегда указывает на ослабление тенденции (в это время входить в рынок не рекомендуется).
          <br/> 3. Если линии +ДЛ и -ДЛ расходятся, то динамика тренда усиливается, и наоборот.
          <br/> 4. Пересечение линий +ДЛ и -ДЛ является очень важным сигналом, так как может предшествовать началу сильного тренда.
          <br/> 5. Если +ДЛ выше -ДЛ, то тренд восходящий (бычий) и следует играть только на повышение (т.е. покупать).
          <br/> 6. Если -ДЛ выше +ДЛ, то тренд нисходящий (медвежий) и следует играть только на понижение (т.е. продавать).
          <br/> 7. Наилучший момент для игры на повышение, когда +ДЛ и АДХ выше, чем -ДЛ, и АДХ при этом возрастает.
          <br/> 8. Наилучший момент для игры на понижение, когда -ДЛ и АДХ выше +ДЛ, и АДХ при этом возрастает.
          <br/> 9. Если АДХ опускается ниже обеих ДЛ, то это свидетельствует о стагнации (консолидации) рынка, которая может оказаться &quot;затишьем перед бурей&quot;.</p>
