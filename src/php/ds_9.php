        <h1>ДС – Дирекционная система (Directional  sistem)</h1>
        <p>Дирекционную систему (ДС) разработал в середине 70-х годов Дж. Вэлс Уайлдер-младший. Данный трендовый индикатор позволяет выявлять тенденции и определять их потенциал.
          <br/> Алгоритм расчета индикатора ДС опускаем, так как он не столько сложен, сколько громоздок.
          <br/> Напомним, что строить индикатор не нужно, он размещается ниже ценового графика в готовом виде (см. рис.).</p>
        <p>Индикатор строится в виде 2-х дирекционных линий (+DЛ и -DЛ &mdash; smoothed directional lines) и третьей линии &mdash; среднего дирекционного индикатора (АДХ &mdash; average directional indicator):</p>
        <ul>
          <li>— первая идет в направлении тренда (+DЛ, красная линия)</li>
          <li>— вторая &mdash; в направлении противоположном тренду (-DЛ, синяя линия)</li>
          <li>— третья рассчитывается как модуль разницы между линиями +/-DЛ (АДХ, черная линия) </li>
        </ul>
