        <p>На рисунках 2, 3 и 4 показаны ситуации, которые, с точки зрения теории волн Эллиотта, являются невозможными.</p>
        <p>Вслед за первым полным циклом, состоящим их 8 волн (5 движущих и 3 корректирующих) следует второй полный цикл, а за ним по Эллиотту – третий. Затем начинается корректирующее движение и т.д.
          <br>Кроме того, существуют еще другие виды волн, подробно описанные Эллиоттом , а именно:
          <br>— зигзаги
          <br>— плоскости
          <br>— треугольники
          <br>— двойные тройки и тройные тройки (комбинированные структуры).</p>
        <p>Словом, черт ногу сломит.</p>
        <p>На самом деле, для того, чтобы практически использовать некоторые познания в области теории Эллиотта, достаточно иметь представление о пятиволновой базисной модели рынка. Она, действительно, может помочь в анализе рынка и принятии конкретных решений по открытию длинных или коротких сделок. Как это сделать?
          <br>Для того чтобы это сделать, необходимы не только знания, но и некоторый практический опыт, которые помогут разглядеть зарождение нового тренда. Если предположить, что тренд начинается с первой волны, то весьма желательно дождаться завершения ее коррекции на 2-ой корректирующей волне.
          <br>Проще говоря, необходимо дождаться завершения 2-ой корректирующей волны. Как только откат завершается, следует входить в рынок. Это позволит «оседлать», как правило, самую длинную 3-ю волну. Если это удается, то ваш счет может значительно пополниться.
          <br>Еще раз повторим, что это не просто и требует определенных знаний и навыков, но возможно.
          <br>Подробно процесс подобного входа в рынок описан в разделе <a href="torg.php">«Торговые стратегии»</a>.</p>
