        <h1>Волновая теория Эллиота</h1>
        <p>Ральф Н. Эллиотт (Ralph Nelson Elliott), используя данные фондового рынка, открыл, что хаотичное движение цен на самом деле подчиняется определенным закономерностям и постоянно повторяет одну и ту же траекторию, один и тот же структурированный рисунок. На основе этого открытия он разработал волновую теорию рыночного анализа, названную его именем.
          <br>Эллиотт выделил несколько типовых моделей движения цен или «волн», которые снова и снова повторяются по форме в хаосе рыночной стихии. Он систематизировал и упорядочил свои наблюдения, дал названия этим моделям и проиллюстрировал их. Так появился закон, который носит его имя - "Закон волн Эллиотта".
          <br>Некоторые авторы полагают, что Закон волн Эллиотта - это «каталог моделей изменения цен, набор эмпирически выведенных правил и указаний для объяснения поведения рынка». Он дает возможность глубоко анализировать состояние рынка и, что самое главное, позволяет во многих случаях предвосхищать его дальнейшее движение.
          <br>В настоящее время Закон волн Эллиотта широко применяется трейдерами в повседневной работе.
          <br>Мы не ставим перед собой задачи подробно, во всех деталях описать Закон волн. И, если честно, - не считаем все эти детали важными для практической работы в реальном рынке (хорошо, что продвинутые не слышат). Но основные фундаментальные понятия этого закона, которые реально помогут в объективном анализе рынка, постараемся осветить. Тех же, кто хочет более подробно познакомиться с теорией Эллиотта, отсылаем к странице <a href="literatura.html">Литература</a>.</p>
        <h2>Важнейшие принципы</h2>
        <p>Развитие рыночных цен, в конечном счете, принимает фигуру, состоящую из пяти волн (рис 1). Три из них, помеченные цифрами 1, 3 и 5 совершают направленное движение. Они разделяются двумя откатами — волнами противоположного направления, которые помечены цифрами 2 и 4, как показано на рис.1.
          <br>Откаты также являются неотъемлемой частью общего направленного движения.</p>
        <div class="img">
          <img src="img/pic6.png" width="288" height="217">
          <p>Рис 1</p>
        </div>
        <p>Пятиволновая модель является базисной моделью движения рынка.
          <br>Рынок всегда находится в рамках базовой пятиволновой модели.
          <br>Волны 1, 3 и 5 называются "импульсными волнами".
          <br>Волны 2 и 4 - "коррекционные волны".
          <br>Волна 2 "корректирует" волну 1, а волна 4 "корректирует" волну 3.
          <br>Последовательность волн от 1 до 5 "корректируется" последовательностью a-b-c.
          <br>Таким образом, полный цикл состоит из 8 волн.</p>
        <p>Существуют 3 правила, считающиеся "нерушимыми":</p>
        <p>— Волна 2 никогда не возвращается дальше начальной точки волны 1.
          <br>Если импульсные волны устремлены вверх, то волна 2 не может опуститься ниже начальной точки волны 1 (см. рис. 2). Если импульсная последовательность идет вниз, то волна 2 не может подняться выше пика, с которого началось развитие волны 1.</p>
        <div class="img">
          <img src="img/pic7.png" width="232" height="269">
