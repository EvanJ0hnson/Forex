        <h1>Свечи и индикаторы</h1>
        <p>Осцилляторы — это созданные по определенному математическому алгоритму индикаторы, которые являются достаточно объективным средством анализа рынка, имеют широкое применение и лежат в основе многих торговых систем. Весьма полезно использовать осцилляторы в совокупности с японскими свечами.</p>
        <h2>Свечи и скользящие средние</h2>
        <p>Скользящие средние СС — это один из старейших и наиболее эффективных инструментов технического анализа.
          <br>Посмотрим, как можно использовать инструмент СС в совокупности с японскими свечами.
          <br>Рассмотрим хорошо известную комбинацию из 2-х СС с различным периодом.
          <br>Короткие и длинные СС по разному реагируют на изменения цен, поэтому возникают ситуации, когда они пересекаются.</p>
        <p>Если короткая СС пересекает длинную СС снизу вверх, то это является характерным бычьим сигналом, который называют «золотой крест».
          <br>Обратная ситуация, когда короткая СС пересекает длинную СС сверху вниз, называется «мертвый крест» и является медвежьим сигналом.</p>
