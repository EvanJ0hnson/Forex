        <h2>Осциллятор RSI</h2>
        <p>RSI (relative strength index) — индекс относительной силы.
          <br>Он используется, в том числе, и в качестве индикатора расхождений, когда цены и осциллятор движутся в разных направлениях. Факт расхождения (дивергенции) может заранее сообщить трейдеру о возможном окончании текущей ценовой тенденции.</p>
        <p>Есть 2 вида расхождений:</p>
        <p>1. Отрицательное, или медвежье (negative bearish divergence) наблюдается тогда, когда цены достигают нового max, а осциллятор — нет. Это говорит о возможном падении рынка.
          <br>2. Положительное, или бычье расхождение (positive bullish divergence) наблюдается тогда, когда цены достигают нового min, а осциллятор — нет. Это означает, что рынок созревает для очередного подъема.</p>
        <p>Расхождения осциллятора RSI более значимы, если они возникают в области перекупленности\перепроданности.</p>
        <p><strong><em>Cигналы осциллятора следует использовать для размещения позиций в направлении господствующей тенденции.</em></strong></p>
        <p>Так по бычьему сигналу осциллятора можно покупать только в том случае, если основная тенденция восходящая и закрывать короткие позиции, если она нисходящая.
          <br>По медвежьему сигналу открывать короткие позиции следует лишь в том случае, когда основная тенденция нисходящая и закрывать длинные позиции, если она восходящая.</p>
