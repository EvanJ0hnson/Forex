        <h2>Свечи и скользящие  средние</h2>
        <p> Скользящие средние СС &mdash; это один из старейших и наиболее эффективных инструментов технического анализа.
          <br /> Посмотрим, как можно использовать инструмент СС в совокупности с японскими свечами.
          <br /> Рассмотрим хорошо известную комбинацию из 2-х СС с различным периодом.
          <br /> Короткие и длинные СС по разному реагируют на изменения цен, поэтому возникают ситуации, когда они пересекаются. </p>
        <p>Если короткая СС пересекает длинную СС снизу вверх, то это является характерным бычьим сигналом, который называют &quot;золотой крест&quot;.
          <br /> Обратная ситуация, когда короткая СС пересекает длинную СС сверху вниз, называется &quot;мертвый крест&quot; и является медвежьим сигналом.</p>
