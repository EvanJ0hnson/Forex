        <h2>Свечи и объем</h2>
        <p>При росте объема текущая тенденция (восходящая или нисходящая) должна продолжиться. Если же развитие тенденции сопровождается снижением объема, то вероятность ее продолжения уменьшается.
          <br>Объем ценен как подтверждающий сигнал при образовании вершин и впадин (оснований).
          <br>Проверка уровня поддержки S при малом объеме — бычий сигнал.</p>
