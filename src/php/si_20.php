        <h2>Стохастический осциллятор </h2>
        <p>Стохастический осциллятор (СТО) &mdash; еще один распространенный инструмент в арсенале трейдеров.
          <br /> Большинство используют его как инструмент для выявления расхождений в сочетании с показаниями перекупленности\перепроданности.
          <br /> Кроме того, некоторые аналитики требуют соблюдения дополнительного условия, согласно которому сигнал к покупке возникает тогда, когда кривая %К пересекает кривую %Д снизу вверх в зоне перепроданности (СТО ~ 20% - 30%), а сигнал к продаже тогда, когда %К пересекает %Д сверху вниз в зоне перекупленности (СТО ~ 70% - 80%).</p>
        <p>Таким образом, сигнал к покупке возникает когда:</p>
        <p>1. рынок &quot;перепродан&quot; (СТО ниже 30%)
          <br /> 2. налицо положительное, или бычье расхождение
          <br /> 3. %К пересекает %Д снизу вверх</p>
        <p>Сигнал к продаже возникает, если:</p>
        <p>1. рынок перекуплен (СТО выще 70%)
          <br /> 2. налицо отрицательное, или медвежье расхождение
          <br /> 3. %К пересекает %Д сверху вниз</p>
        <p>Как и другие осцилляторы, СТО удобно и полезно использовать в комбинации с японскими свечами.</p>
