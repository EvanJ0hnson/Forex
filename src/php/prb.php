        <h1>Parabolic SAR</h1>
        <h2>Технический индикатор Parabolic SAR (параболическая Система SAR, Stop & Revers).</h2>
        <p>Индикатор Parabolic SAR (или Параболик) был разработан Уэллсом Уайлдером младшим для анализа трендовых рынков (SAR - от английского stop-and-reversal - остановка и разворот).</p>
        Его используют:
        <br/> во-первых, для определения направления тренда
        <br/> во-вторых, для определения момента разворота тренда (для определения момента закрытия открытых ранее позиций)
        <br/> в-третьих, в качестве линии скользящего стопа (trailing stop)</p>
        <h2>Расчет  индикатора параболик (Parabolic SAR):</h2>
        <p>Параболик для длинных позиций:
          <br /> SARi = (HIGHi-1&nbsp;- SARi-1) * ASS + SARi-1&nbsp;
          <br /> Параболик для коротких позиций:
          <br /> SARi = (LOWi-1 - SARi-1) * ASS - SARi-1&nbsp;
          <br /> SARi&nbsp;&ndash; значение параболика;
          <br /> HIGHi-1&nbsp;&ndash; наибольшее значение цены предыдущего периода;
          <br /> LOWi-1 &ndash;&nbsp;наименьшее значение цены предыдущего периода;
          <br /> SARi-1&nbsp;&ndash; значение Parabolic SAR предыдущего периода;
          <br /> ASS&nbsp;&ndash; фактор ускорения (шаг изменения цены закрытия позиции, рекомендуемое значение 0,02).
          <br /> Фактор ускорения (ACCELERATION) вызывает сближение Parabolic SAR и цены. Причем, индикатор приближается к цене, тем быстрее, чем быстрее растет или падает цена.
          <br /> Parabolic SAR наносится на ценовой график. На бычьем тренде он располагается ниже цен, а на медвежьем рынке &ndash; выше цен (см. рис.). </p>
