        <p>Parabolic SAR не рекомендуется использовать для определения оптимальной точки входа в рынок, но использовать его показания при открытии сделок имеет смысл. Способ торговли с помощью индикатора Parabolic SAR достаточно прост, а именно:</p>
        <ol>
          <li>Если уровень цены выше индикатора&nbsp;-&nbsp;&nbsp;можно открывать длинные позиции.</li>
          <li>Если&nbsp;уровень цены ниже индикатора - можно открывать короткие позиции. </li>
        </ol>
        <p>Можно сформулировать более осторожно:</p>
        <ol>
          <li>Если уровень цены выше индикатора&nbsp;-&nbsp;&nbsp;не следует открывать короткие позиции, т.е. продавать.</li>
          <li>Если&nbsp;уровень цены ниже индикатора &ndash; не следует открывать длинные позиции, т.е. покупать</li>
        </ol>
        <p>Индикатор Parabolic SAR не дает четкой стратегии входа в рынок, зато очень хорошо определяет точки выхода из рынка, т.е. моменты фиксации прибыли (см. рис.): </p>
        <ol>
          <li>Длинные позиции следует закрывать, когда цена опускается ниже линии&nbsp; &nbsp;Parabolic SAR.</li>
          <li>Короткие позиции следует закрывать, когда цена поднимается выше линии Parabolic SAR.</li>
        </ol>
        <p>Проще говоря, как только индикатор поменял свое положение относительно цены, следует закрыть ранее открытые позиции.
          <br />
        </p>
        <p>Часто данный индикатор используют в качестве линии скользящего стопа (trailing stop): </p>
        <ol>
          <li>Если ордер открыт на покупку и цены продолжают расти, тогда вместе с растущими ценами повышается и Parabolic SAR, подсказывая, на какой уровень нужно перенести защитную приостановку S/L.</li>
          <li>Если ордер открыт на продажу и цены продолжают падать, тогда вместе с падающими ценами понижается и Parabolic SAR, подсказывая на какой уровень нужно перенести защитную приостановку S/L .</li>
        </ol>
        <p>Данный индикатор дает простую и очень эффективную систему увеличения прибыли, что позволяет воплотить в жизнь один из главных принципов технического анализа &ndash; &rdquo;дай прибыли накапливаться&rdquo;.
