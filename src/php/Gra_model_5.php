        <h1>Графические  модели</h1>
        <p>Графические модели помогают предугадать судьбу тенденции: ее продолжение или разворот. Существуют 2 основные разновидности моделей:</p>
        <p>— модели продолжения <em>(continuation patterns)</em></p>
        <p>— модели разворота <em>(reversel patterns)</em></p>
        <p>Модели продолжения включают в себя флажки (flags) и вымпелы (pennants). Модели разворота включают в себя конфигурацию &quot;голова и плечи&quot; (head-and-shoulders), ее перевернутый вариант (inverse head-and-shoulders), двойные (тройные и более) гребни и донышки (double tops and bottoms), а также &rdquo;клинья&rdquo;.</p>
        <p>Некоторые конфигурации могут быть моделями как продолжения, так и разворота – это треугольники (triangles) и прямоугольники (rectangles).</p>
        <div class="img">
          <img src="img/r14.gif" width="433" height="183">
        </div>
        <p>Если несколько графических моделей подтверждают друг друга, можно подумать об открытии позиции. Если они опровергают друг друга от — игры лучше воздержаться.</p>
        <h2>Модели продолжения</h2>
        <p><strong>Флаг</strong> (флажок) — это параллелограмм, параллели которого направлены по восходящей или нисходящей.</p>
        <div class="img">
          <img src="img/r15.gif" width="410" height="274">
        </div>
        <p>Флажки с наклоном против тенденции представляют собой модели продолжения. При этом, ход ценового графика до возникновения ”флажка” — X, равен ходу после прорыва модели — Y, т.е. X = Y. Флажок с наклоном в сторону тенденции указывает на ее истощение.</p>
        <p><strong>Вымпел</strong> — это маленький треугольник с наклоном сторон в одном направлении (см. рис.). Вымпелы с наклоном против тенденции представляют собой модели продолжения. При этом, также как и для флага, ход ценового графика до возникновения &quot;вымпела&quot; — X, равен ходу после прорыва модели — Y, т.е. X = Y. Вымпел с наклоном в сторону тенденции указывает на ее истощение.</p>
        <p><strong>Прямоугольник</strong> — это графическая модель, отражающая движение цен между 2-я параллельными линиями. Обычно они горизонтальны, но могут восходить и нисходить.</p>
        <div class="img">
          <img src="img/r16.gif" width="508" height="238">
        </div>
        <p>Прямоугольники и треугольники могут быть моделями продолжения или разворота. </p>
        <p>Для построения прямоугольника требуется 4-е точки: 2 вершины для построения верхней параллели и 2 донышка для построения нижней параллели.</p>
        <p>Верхняя линия прямоугольника представляет собой уровень сопротивления R, а нижняя — уровень поддержки S. Прямоугольник свидетельствует о равновесии сил быков и медведей. Главный вопрос в том, кто из них, в конце концов, победит, т.е. в каком направлении произойдет прорыв.</p>
        <p>Если объем сделок возрастает, когда цены приближаются к верхней границе прямоугольника (R), то вероятнее верхний прорыв, а если он возрастает с приближением цен к нижней границе (S), то вероятнее нижний прорыв.</p>
        <p><strong><em>Прорыв линии R или S на скудном объёме, скорее всего, ложный.</strong></em>
        </p>
        <p><strong> <em>Чем длиннее прямоугольник, тем значительнее прорыв из него.</strong></em>
        </p>
        <p>Особенно значимы прорывы на недельных графиках. </p>
        <p>Есть несколько методов определения вероятной мишени прорыва (<em>min</em> цели). Можно измерить высоту прямоугольника и спроецировать ее от прорванной границы в направлении прорыва, тогда min цель определится из соотношения X = Y (см. рис).</p>
        <p>Есть еще один способ определения хода ценового графика после прорыва прямоугольника: измерить высоту прямоугольника X, умножить ее на один из коэффициентов Фибоначи (1,618, 2,618 и 4,236) и спроецировать полученную величину в направлении прорыва.</p>
        <p><strong>Правила игры:</strong> </p>
        <p>1. Покупайте у нижней границы и продавайте на понижение у верхней. Индикаторы, такие как <a href="sto_11.php" target="_blank">стохастический осциллятор</a> и прочие (см. навигацию) помогут определить момент надвигающегося разворота цен. При купле у нижней границы прямоугольника (S) размещайте защитную приостановку чуть ниже нее (т.е. ниже S).</p>
        <div class="img">
          <img src="img/r17.gif" width="422" height="201">
        </div>
        <p>При продаже на понижение возле верхней рамки прямоугольника размещайте защитную приостановку чуть выше верхней границы (т.е. выше R).</p>
        <p>2. Для определения вероятности либо верхнего, либо нижнего прорыва анализируйте рынок в более крупном масштабе времени по сравнению с тем, в котором играете. Вероятнее всего прорыв произойдет в направлении тенденции более крупного графика.</p>
        <p>3. При купле после верхнего прорыва или продаже после нижнего прорыва защитную приостановку следует размещать внутри прямоугольника, т.к. цены могут вернуться к границе прямоугльника на малом объеме.</p>
