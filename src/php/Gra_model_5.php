        <strong>Правила игры:</strong>
        <p>1. Чтобы определить, какой прорыв следует ожидать – вверх или вниз, необходимо обратиться к графику большего масштаба. Прорыв более вероятен в сторону тренда графика большего масштаба.
          <br> 2. Покупая при верхнем прорыве, прорыве размещайте приказ о купле чуть выше верхней границы треугольника. Продавая при нижнем прорыве, размещайте приказ о продаже чуть ниже нижней границы. И в том и в другом случае защитную приостановку следует ставить внутри треугольника.</p>
        <h2>Модели разворота </h2>
        <p><strong>Голова и плечи (head—and—shoulders).</strong></p>
        <p>Модель состоит из 3-х вершин, центральная из которых, выше, чем боковые.</p>
        <div class="img">
          <img src="img/r23.gif" width="528" height="261">
        </div>
        <p>Эта модель обычно возникает в конце длительного восходящего тренда и указывает на завершение тенденции к повышению. Центральную самую высокую вершину называют &quot;головой&quot;. Вершины пониже — &quot;плечи&quot;. У них имеется и &quot;шея&quot;, линия которой проходит через донышки спадов левого плеча и головы. Эта линия не всегда горизонтальна: она может быть и восходящей и нисходящей.
          <br> Правое плечо может быть выше или ниже, шире или уже левого. Падение цен от правого плеча с ломкой линии шеи знаменует конец тенденции к повышению. Проломив линию шеи, цены иногда возвращаются к ней на малом объеме. Этот подъем создает превосходные возможности для игры на понижение с защитной приостановкой непосредственно над линией шеи.
          <br> Модель &quot;голова и плечи&quot; имеет характерные объемы сделок. Очень часто объем в области головы ниже, чем у левого плеча, а объем в области правого плеча еще ниже, чем в области головы. При ломке шеи он обычно возрастает, а при возвращении к ней — уменьшается.</p>
        <p>Модель &quot;голова и плечи&quot; позволяет приблизительно определить мишень новой тенденции к понижению, т.е. min величину хода ценового графика в сторону пробоя линии шеи. Для этого необходимо измерить расстояние между макушкой головы и линией шеи (X) и спроецировать его вниз от линии шеи (Y). X = Y. Зачастую ход ценового графика превышает min цель.</p>
        <p>Перевернутая модель &quot;голова и плечи&quot; является зеркальным отражением модели &quot;голова и плечи&quot;. Выводы, сделанные в отношении последней, справедливы и для перевернутой модели, но с противоположным знаком.</p>
        <h2>Двойные вершины и впадины</h2>
        <p>Двойные вершины образуются, когда цены подскакивают в зону предыдущего max. Двойные впадины, — когда цены падают до предыдущего <em>min</em>. Вторая вершина или впадина может быть чуть выше или ниже первой.</p>
        <div class="img">
          <img src="img/r24.gif" width="539" height="180">
        </div>
        <p>Для выявления 2-х вершин/впадин удобно использовать технические идикаторы, т.к. именно в этих точках часто возникает дивергенция, т.е. расхождения между ценами и индикаторами. На практике также встречаются и тройные вершины/впадины. Они также являются<strong> моделями разворота</strong>.</p>
        <h2>Клинья</h2>
        <p>Клинья также относятся к моделям разворота. Клинья имеют края, скошенные по направлению друг к другу. Клинья могут быть направлены вверх или вниз.</p>
