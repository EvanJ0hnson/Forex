        <p><strong>Линейка</strong> — это разновидность прямоугольника, а именно, узкая, горизонтальная зона изменения цен, отражающая консолидацию рынка.</p>
        <div class="img">
          <img src="img/r20.gif" width="328" height="71">
        </div>
        <p>Играть в такой зоне практически невозможно. Ее можно трактовать как &quot;затишье перед бурей&quot;, то есть мощным движением рынка в ту или иную сторону. </p>
        <p><strong>Треугольник</strong> — это область изменения цен, верхняя и нижняя границы которой сходятся с правой стороны. Треугольники можно разделить на три группы:</p>
        <p>равнобедренные
          <br> восходящие
          <br> нисходящие
        </p>
        <div class="img">
          <img src="img/r21.gif" width="583" height="314">
        </div>
        <p>Треугольники могут иметь плоские вершины, плоские основания или покатые стороны, как у равнобедренного треугольника. Рано или поздно быки или медведи, одержат победу и произойдет пробой одной из сторон треугольника. Мишень, или min цель для каждого треугольника определяется из соотношения X = Y. Зачастую ход ценового графика после пробоя превосходит min мишень.</p>
        <p>Треугольник может быть моделью разворота и моделью продолжения. Некоторые треугольники просто переходят в игровые диапазоны, т.е. в боковой тренд.</p>
        <p>У равнобедренных треугольников верхняя и нижняя линия сходятся под одинаковым углом по отношению к горизонтали, они чаще всего бывают моделями продолжения.</p>
        <p>Восходящий треугольник имеет относительно горизонтальную верхнюю сторону и направленную вверх нижнюю сторону. Восходящий треугольник чаще оборачивается прорывом вверх.
          <br> Нисходящий треугольник имеет сравнительно горизонтальную нижнюю сторону и направленную вниз верхнюю сторону. Нисходящий треугольник чаще приводит к прорыву вниз.</p>
