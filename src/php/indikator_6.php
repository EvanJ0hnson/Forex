        <h1>Индикаторы</h1>
        <p>Индикаторы позволяют более объективно, чем графические модели выявлять тенденции и их развороты. Причем, одни индикаторы лучше улавливают тенденции, другие лучше работают в игровых диапазонах (боковых трендах).</p>
        <p>Торговая платформа Metatrader располагает целым арсеналом различных индикаторов, каждым из которых трейдер может воспользоваться для анализа рынка. Одни индикаторы располагаются под ценовым графиком (например, индекс относительной силы RSI), другие — накладываются непосредственно на ценовой график (например, скользящие средние СС).</p>
        <p>Индикаторы делят на две группы:</p>
        <p>— индикаторы тенденций (trend-following indicators)</p>
        <p>— осцилляторы (oscillators), или «сигнальщики»</p>
        <p><b>Индикаторы</b> тенденций отличное средство анализа рынка, который движется вверх или вниз, но при его застое их сигналы ненадежны.</p>
        <p><b>Осцилляторы</b> прекрасно улавливают перемены на застойных рынках, но с установлением тенденции они подают преждевременные и даже ложные сигналы. </p>
        <h2>Индикаторы тенденций    это:</h2>
        <p>— скользящие средние (СС — moving averages)</p>
        <p>— схождение-расхождение скользящих средних — МАСД <em>(moving averages convergence-divergens)</em></p>
        <p>— дирекционная система (directional system) и т.д.</p>
        <p>Индикаторы этой группы подают синхронные или запаздывающие сигналы, т.е. одновременно или после разворота тенденции.</p>
        <h2> Осцилляторы  помогают определить поворотные моменты.</h2>
        <p>К ним относятся:</p>
        <p>— индекс относительной силы <em>(RSI)</em></p>
        <p>— стохастический индекс <em>(stochastic)</em></p>
        <p>— инерция <em>(momentum)</em></p>
        <p>— индекс силы <em>(force index)</em> и проч.</p>
        <p>Индикаторы этой группы подают опережающие или синхронные сигналы и нередко появляются до изменения цен.</p>
        <h2>Типы расхождений</h2>
        <p>Осцилляторы  подают наиболее сильные сигналы при расхождении с ценами.
          <br> Расхождения донышек возникают при нисходящей тенденции: они выявляют готовность рынка к развороту вверх. Эти расхождения образуются, когда цены опускаются до новой глубины, а осциллятор не следует их примеру.
          <br> Расхождения гребней наблюдаются при восходящей тенденции: они выявляют готовность рынка к развороту вниз. Эти расхождения образуются, когда цены поднимаются до новой высоты, а осциллятор не следует их примеру. </p>
        <p>Э. Найман отмечает 3 типа расхождений как для донышек, так и для гребней:</p>
        <p>— расхождения типа А выявляют важные поворотные моменты, т.е. оптимальные спекулятивные возможности</p>
        <p>— расхождения типа В менее значимы</p>
        <p>— расхождения типа С наименее существенны.</p>
        <h2>Расхождения типа А </h2>
        <p>Расхождения гребней типа А образуются, когда цены достигают новой высоты, а гребень сигнальщика ниже предыдущего. За подобными расхождениями обычно следуют резкие спады.</p>
