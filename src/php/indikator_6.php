        <p>Расхождения донышек типа В наблюдается, когда цены образуют двойное донышко (двойную впадину), но второе донышко сигнальщика выше первого.</p>
        <h2>Расхождения типа С </h2>
        <p>Расхождения гребней типа С образуются, когда цены достигают нового гребня (новой вершины), а индикатор останавливается на той же высоте, что и в предыдущий подъем, т.е. образует двойную вершину.</p>
        <div class="img">
          <img src="img/r39.gif" width="485" height="235">
        </div>
        <p>Расхождения донышек типа С наблюдаются, когда цены падают на новую глубину, а индикатор образует двойное донышко (двойную впадину), т.е. остается на той же глубине, что и предыдущий спад.</p>
        <p>Расхождения типа А указывают, как правило, на возможность выгодных сделок. Расхождения типов В и С чаще оборачиваются зигзагами, и если их сигналы не подтверждены другими надежными индикаторами, их лучше проигнорировать.</p>
        <p>Тройные расхождения донышек или гребней состоят из 3 донышек цен и сигнальщика или 3 гребней цен и сигнальщика. Эти расхождения даже сильнее обычных. </p>
