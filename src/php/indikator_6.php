        <p>Расхождения донышек типа А образуются, когда цены падают на новую глубину, а донышко сигнальщика выше предыдущего. Эти расхождения нередко предшествуют резким взлетам. </p>
        <h2>Расхождения типа В</h2>
        <p>Расхождения гребней типа В наблюдается, когда цены образуют двойную вершину, но при этом второй гребень (вершина) сигнальщика ниже первого.</p>
