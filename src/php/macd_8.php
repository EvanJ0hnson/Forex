        <p>Пересечение линий является сигналом на покупку или продажу. </p>
        <p>Стандартные периоды средних скользящих — 12 и 26, сигнальная линия имеет период 9.</p>
        <p>Но самый сильный сигнал гистограмма РСС подает при расхождении с ценами (дивергенции). Появление подобного сигнала может привести к развороту тренда.</p>
        <p> 1. Если цена достигла нового пика, а пик гистограммы ниже предыдущего, то имеет место расхождение гребней ( медвежье расхождение — bearish divergence ), которое сигнализирует о возможном развороте тренда. А это значит, что следует закрывать длинные позиции и искать удобный момент для игры на понижение, т.е. продажи.</p>
