        <h1>Японские
        свечи </h1>
        <p>Наряду с традиционным классическим техническим анализом на основе графических моделей широкое распространение у профессиональных трейдеров получил свечной анализ ценовых графиков, зародившийся и сформировавшийся в средневековой Японии. Наилучшую интерпретацию свечного анализа дал выдающийся финансовый аналитик <a href="">Стив Нисон</a>.</p>
        <p>Вот как выглядят японские свечи:</p>
        <div class="img">
