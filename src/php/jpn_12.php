        <p>Широкая часть свечи называется телом свечи (real body).
          <br> Тело представляет ценовой диапазон торговой сессии между ценами открытия и закрытия ( Цо и Цз ).</p>
        <p> Если тело свечи имеет черный цвет, то это означает, что Цз меньше цены открытия сессии, т.е. Цз &lt; Цо.</p>
        <p>Если тело свечи имеет белый цвет, то, наоборот, Цо &lt; Цз.</p>
        <p>Тонкие линии над телом и под ним называются тенями (shadows).
          <br> Тени указывают экстремальные цены торговой сессии.
          <br> Тень над телом называют верхней тенью (upper shadow), а тень под телом — нижней тенью (lower shadow).</p>
        <p>Высшая точка верхней тени дает значение максимальной цены сессии (Цmax), а низшая точка нижней тени — значение минимальной цены этой сессии (Цmin).
          <br> Если у свечи отсутствует верхняя тень, говорят, что у нее срезана вершина (shaven head), если у свечи отсутствует нижняя тень, говорят, что у нее срезано основание (shaven bottom).</p>
        <div class="img">
          <img src="img/r43.gif" width="266" height="248">
        </div>
        <p>Свечи с малым размером тела называются «волчкам».</p>
