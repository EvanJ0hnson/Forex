        <p>Окна также становятся областями поддержки S и сопротивления R.</p>
        <p>Окно при восходящей тенденции — сигнал дальнейшего подъема цен. При корректирующих спадах такое окно должно оказать поддержку ценам (но, если в результате корректирующего спада окно «закрывается», то предшествующая тенденция считается законченной).</p>
        <p>Окно при нисходящей тенденции - сигнал дальнейшего понижения цен.
          <br>При корректирующих подъемах цены будут встречать сопротивление на уровне окна (но, если окно «закрывается», а рост цен продолжается, то это говорит о том, что нисходящей тенденции пришел конец).></p>
        <p>При восходящей тенденции корректирующие откаты цен к окну можно использовать в качестве области покупки.
