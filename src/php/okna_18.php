        <h1>Модели продолжения</h1>
        <h2>Окна</h2>
        <p>Большинство моделей японских свечей являются моделями разворота. Однако существуют модели, которые являются индикаторами продолжения тенденции. К их числу относятся так называемые «окна» (windows).</p>
        Окно — это ценовой разрыв между экстремальными значениями цены текущего и предшествующего торговых дней.</p>
        <div class="img">
          <img src="img/r75.gif" width="502" height="408">
